// Конфігурація Telegram-бота. Усе, що тут є, можна змінювати.
// Секрети — через env. Решта — правте файл і робіть деплой.

import type { BadgeDef } from '../types.js';

// Дія, за яку марафон може давати підвищені бали.
// 'recognition' — розпізнавання (нова справа з нуля / parallel-сабміт);
// 'verification' — перевірка (підтвердження або виправлення), і в боті, і на сайті.
export type MarathonAction = 'recognition' | 'verification';

// Контур, у якому діє марафон. Dev/прод розрізняються env-змінною TABLE_PREFIX
// (botdev_ → dev/staging, інакше → prod). 'all' (або відсутнє поле) — обидва.
export type MarathonEnv = 'dev' | 'prod' | 'all';

export interface MarathonConfig {
  // Назва марафону для текстів (підставляється в {name}).
  name: string;
  // Початок марафону за київським часом (dispatch.timezone), формат 'YYYY-MM-DD HH:mm'.
  // Можна без часу ('YYYY-MM-DD') — тоді 00:00.
  start: string;
  // Кінець марафону за київським часом, формат 'YYYY-MM-DD HH:mm' (момент НЕ включно).
  end: string;
  // Множник балів за дії з actions у цей проміжок (стакається з денним множником ×1/×1.5/×2).
  coefficient: number;
  // Які дії беруть участь у марафоні.
  actions: MarathonAction[];
  // На якому контурі діє марафон. За замовч. (undefined) — на обох (dev і prod).
  env?: MarathonEnv;
}

export interface TelegramBotConfig {
  tg: {
    botTokenEnv: string;        // назва env-змінної з токеном
    channelIdEnv: string;       // назва env-змінної з ID приватного каналу
    webhookSecretEnv: string;   // секрет webhook
  };
  supabase: {
    urlEnv: string;
    serviceKeyEnv: string; // service_role key (повний доступ)
  };
  cronSecretEnv: string;        // секрет для зовнішнього cron-тригера
  adminLoginEnv: string;        // логін адміна
  adminPasswordEnv: string;     // пароль адміна

  dispatch: {
    startHourKyiv: number;      // 0..23
    endHourKyiv: number;        // 0..23 (включно)
    intervalHours: number;      // крок між розсилками
    timezone: string;           // 'Europe/Kyiv'
    sessionTtlHours: number;    // термін життя стану діалогу
    unansweredPauseAfter: number; // n пропусків поспіль → авто-пауза
    skipIfSessionOpen: boolean; // не слати нову, якщо є відкрита сесія
  };

  cases: {
    targetSubmissions: number;   // мін. кількість підтверджень для "done"
    allowExtraAfterTarget: boolean;
    dailyGoal: number;           // денний план опрацьованих справ (для повідомлення подяки)
  };

  // Загальна мета — для відображення прогнозу завершення фонду на сторінці «Результати».
  fund: {
    number: string;              // номер фонду (для тексту), напр. '442'
    totalDescriptions: number;   // скільки описів у фонді разом
    baselineDoneDescriptions: number; // вже розпізнано до початку роботи бота
  };

  verif: {
    opysBaseUrl: string;         // база URL для посилання на повний PDF опису (веб-перевірка)
  };

  // Групи Telegram, у які Блукач шле ранкові/вечірні/підсумкові оголошення.
  // ID супергруп — від'ємні з префіксом -100. Можна додавати/прибирати без редеплою БД.
  groupChats: {
    announceChatIds: string[];
  };

  points: {
    base: number;
    tier1: { thresholdInclusive: number; multiplier: number };
    tier2: { thresholdInclusive: number; multiplier: number };
  };

  // Марафони. У задані дні бали за вказані дії множаться на coefficient.
  // Дати — у конфізі (не в БД). Можна задати кілька марафонів; на конкретну дату
  // береться перший зі списку, що містить цю дату.
  marathons: MarathonConfig[];

  sheets: {
    resultsSheetName: string;
    metaSheetName: string;
    usersSheetName: string;
    casesSheetName: string;
    sessionsSheetName: string;
    dailyScoresSheetName: string;
    dispatchLogSheetName: string;
    sourceLink: {
      mode: 'telegram_message' | 'custom_url' | 'none';
      // {channelId} — числовий ID без -100; {messageId} — id повідомлення; {caseId}; {pdfUrl}; {page}
      template: string;
      columnLabel: string;
    };
    // Службові колонки, що додаються в Results перед колонками з tableStructure.
    serviceColumnsBefore: string[];
    // Службові колонки після.
    serviceColumnsAfter: string[];
  };

  slicing: {
    defaultMode: 'manual' | 'auto';
    geminiModel: string;
    autoModel: string; // legacy, дублює geminiModel
    detectionStrategy: 'boxes' | 'separators';
    autoPrompt: string;
    autoPromptSeparators: string;
    renderScale: number;
    imageMaxWidthPx: number;
    jpegQuality: number;
    bboxPaddingX: number;
    bboxPaddingY: number;
    alignBoxesHorizontally: boolean;
  };

  texts: Record<string, string>;

  // Випадкові тексти, які надсилаються перед розсилкою справи за розкладом.
  scheduledGreetings: string[];

  // Мотиваційні повідомлення для апгрейду рівня множника балів.
  // tier1: при переході в tier1 (одноразово в межах дня).
  // tier2: при переході в tier2 (одноразово) + ПІСЛЯ КОЖНОГО підтвердження
  // на найвищому рівні.
  tierMessages: {
    tier1: string[];
    tier2: string[];
  };

  // Шаблони для оголошень у групи (морнінг-топ, вечір-пазл, опис-завершено).
  // Кожен масив — варіанти, з яких випадково обирається один на повідомлення.
  // Підстановки: {date} {dateYesterday} {leaders} {place} {name} {count}
  //              {winners} {sentence} {archive} {fund} {opys} {descName}
  //              {totalCases} {totalDone} {donePct}
  groupAnnounce: {
    morningHeader: string[];
    morningLine: string;        // один рядок ліст-айтема. {place} {name} {count}
    morningEmpty: string[];     // якщо вчора ніхто нічого не робив
    // Вечірній підсумок Описового пазла — три гілки залежно від кількості переможців:
    eveningPuzzleAll: string[];      // 3/3 призів — повний комплект
    eveningPuzzleSome: string[];     // 1 або 2 переможці — лишилось ще {remainingPlaces} місць
    eveningPuzzleNobody: string[];   // 0 переможців — іронія + заохочення
    eveningWinnerLine: string;       // {place} {name} {points}
    descriptionDone: string[];  // опис закрито на 100%
  };

  // Каталог бейджів (досягнень). Можна доповнювати з часом — кожен бейдж
  // користувач отримує один раз і назавжди. Картинки лежать у api/telegram/badges/.
  badges: BadgeDef[];

  // Гра «Описовий пазл»: щодня адмін задає фразу, гравці збирають її слова,
  // розпізнаючи справи. Перші троє, хто зібрав і підтвердив усю фразу за день,
  // отримують призові бали.
  puzzle: {
    // Призи за 1/2/3 місце. УВАГА: мають збігатися з сумами в SQL-функції
    // bot_award_puzzle_winner (вона — джерело істини при нарахуванні).
    prizes: [number, number, number];
    // Коли слово стає підтвердженим:
    // 'single' — на перше підтвердження версії розпізнавача іншим учасником (легше виграти);
    // 'full'   — лише коли справа повністю закрита (набрала всі підтвердження).
    confirmMode: 'single' | 'full';
    // Службові слова, які НЕ вважаємо словами для збору (щоб не закривати пазл
    // тривіальними прийменниками). Порівняння — у нижньому регістрі.
    stopwords: string[];
    // Текст правил (HTML), показується по кнопці «Правила».
    rules: string;
  };
}

